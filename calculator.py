"""
Calcuator library with basic math operations
"""


def add(f_term, s_term):
    return f_term + s_term


def subtract(f_term, s_term):
    return f_term - s_term


def multiply(first_term, second_term):
    return first_term * second_term


def divide(first_term, second_term):
    return first_term / second_term


